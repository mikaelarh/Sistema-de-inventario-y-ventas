    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ((!Cb1.isSelected()) && (!Cb2.isSelected())) {
            JOptionPane.showMessageDialog(null, "Debes seleccionar una opcion");
        } else {
            if (Cb1.isSelected()) {
                bean.setCase1("`Producto`= '" + nombre.getText() + "'");
                guardarNom(bean.getId(), bean.getCase1());
                JOptionPane.showMessageDialog(null, "Cambios Guardados");
                nombre.setText("");
                TP();
            }
            if (Cb2.isSelected()) {
                bean.setCase2("`UID1`= '" + Um1.getSelectedIndex() + "', `Um1`= '" + Um1.getSelectedItem().toString() + "',`P1`='" + precio1.getText() + "',`C1`='" + Cantidad1.getText() + "'");
                guardarInv(bean.getId(), bean.getCase2());
                JOptionPane.showMessageDialog(null, "Cambios Guardados");
                Cb2.setSelected(false);
                Um1.setSelectedIndex(0);
                precio1.setText("");
                Cantidad1.setText("");
                nombre.setText("");
                TP();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

