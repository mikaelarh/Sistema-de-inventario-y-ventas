    private void Encontrar() {
        try {
            con = conexion.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM `inventario` WHERE inventario.Id='" + bean.getId() + "' ORDER BY Producto ASC");
            while (rs.next()) {
                nombre.setText(rs.getString("producto"));
                Um1.setSelectedIndex(rs.getInt("UID1"));
                precio1.setText(rs.getString("P1"));
                Cantidad1.setText(rs.getString("C1"));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

