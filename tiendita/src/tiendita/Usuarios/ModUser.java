    private void cargar() {
        clear();
//        titulos();
        String[] registros = new String[5];
        try {
            con = conexion.getConnection();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM `usuarios`");
            while (rs.next()) {
                registros[0] = rs.getString("IdUsuario");
                registros[1] = rs.getString("Nombre");
                registros[2] = rs.getString("Usuario");
                registros[3] = rs.getString("Contrase単a");
                registros[4] = rs.getString("Rango");
//                registros[2] = "$ " + rs.getString("precio");
                model.addRow(registros);
            }
            TabUsers.setModel(model);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void clear() {
        model = ((DefaultTableModel) TabUsers.getModel());
        while (model.getRowCount() > 0) {
            model.removeRow(0);
        }
    }

    public void GuardarMod(String id, String sql) {

        try {
            con = conexion.getConnection();
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            PreparedStatement ps = con.prepareStatement("UPDATE `usuarios` SET " + sql + "  WHERE `IdUsuario`='" + id + "'"); //siempre poner el primer dato NULL
            int res = ps.executeUpdate();
            if (res > 0) {
                cargar();
                nombre.setText("");
                usuario.setText("");
                contrase単a.setText("");
                cb1.setSelected(false);
                cb2.setSelected(false);
                cb3.setSelected(false);
                JOptionPane.showMessageDialog(null, "Cambios guardados");
            }
            con.close(); //siempre cerrar las conexiones despues de usarlas
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex); // imprime el error si hay
        }
    }

    private void Eliminar(String id) {
        try {
            con = conexion.getConnection();
            Class.forName("com.mysql.jdbc.Driver").newInstance();
            PreparedStatement ps = con.prepareStatement("DELETE FROM `usuarios` WHERE IdUsuario='" + id + "'"); //siempre poner el primer dato NULL
            int res = ps.executeUpdate();
            if (res > 0) {
                PreparedStatement ps2 = con.prepareStatement("UPDATE `usuarios` SET `IdUsuario`=IdUsuario-1 WHERE `IdUsuario`>'" + id + "'");
                int res2 = ps2.executeUpdate();
                cargar();
                nombre.setText("");
                usuario.setText("");
                contrase単a.setText("");
                JOptionPane.showMessageDialog(null, "Usuario Eliminado");
            }
            con.close(); //siempre cerrar las conexiones despues de usarlas
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex); // imprime el error si hay
        }
    }

